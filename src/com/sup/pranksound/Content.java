package com.sup.pranksound;

public class Content {
	
	private String name="";
	private String subname="";
	
	public Content(String mname,String msubname){
		name=mname;
		subname=msubname;
	}
	
	public String getName(){
		return name;
	}
	
	public String getSubname(){
		return subname;
	}
	

}
